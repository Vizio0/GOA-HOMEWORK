# 1 დან 100 მდე დაპრინტეთ ყველა რიცხვი, მაგრამ:
  
# თუ რიცხვი იყოფა 3-ზე -> დაპრინტეთ "Fizz"
# თუ იყოფა 5-ზე -> დაპრინტეთ "Buzz"
# თუ ორივეზე იყოფა -> დაპრინტეთ "FizzBuzz"
# სხვა შემთხვევაში -> თავად რიცხვი

for i in range(1,100):
    if i % 3 == 0 and i % 5 == 0:
        print("fizzbuzz")
    elif i % 3 == 0:
        print("fizz")
    elif i % 5 == 0:
        print("buzz")
    else:
        print(i)
